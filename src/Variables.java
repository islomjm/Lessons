public class Variables {
    public Variables() {
    }

    public static void main(String[] args) {
        int myInt = 557;
        short myShort = 3266;
        long myLong = 12314654L;
        double myDouble = 235.35;
        float myFloat = 2313.5F;
        char c = 'a';
        boolean b = true;
        byte myByte = 100; // -128 - 127
        System.out.println(myInt);
    }
}
